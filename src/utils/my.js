let getBnbBal = (eth) => {
  axios
    .get("https://api.binance.com/api/v3/ticker/price?symbol=BNBETH")
    .then((response) => {
      let a = response.data.price;
      let bal = eth / a;
      return bal;
    })
    .catch((error) => {
      console.log(error);
      return 0;
    });
};

let convertBnbUsd = async (bnb) => {
  let balance = 0;
  try {
    var response = await axios.get(
      "https://api.binance.com/api/v3/ticker/price?symbol=BNBUSDT"
    );
    let a = response.data.price;

    balance = (parseFloat(bnb) * parseFloat(a)) / 1;
    return balance;
  } catch (error) {
    console.log(error);
    return balance;
  }
};

let cleanFloat = (amount) => {
  let clean;
  amount = amount.toString();
  clean = amount.replace(/,/g, "");
  clean = parseFloat(clean);

  return clean;
};

let addDays = (date, days) => {
    var result = new Date(date);
    result.setDate(date.getDate() + days);
    return result;
};

